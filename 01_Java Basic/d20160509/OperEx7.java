package d20160509;
// 7 순위: 삼항 연산자

// OperEx7 Class Start
public class OperEx7 {
	// Main Method Start
	public static void main(String[] args)
	{
		int a = 10;
		int b = 5;
		// (조건) ? "참" : "거짓"
		System.out.println((a>b)? "참":"거짓"); // a가 b보다 크기 때문에 앞쪽의 참 조건을 만족하여 '참' 출력.
		System.out.println((a<b)? "참":"거짓"); // a가 b보다 작기 때문에 앞쪽의 참 조건을 만족하지 않아 '거짓' 출력.
	}
	// Main Method End
}
// OperEx7 Class End
