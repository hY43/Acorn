package d20160509;

// StatementEx3 Class Start
public class StatementEx3 {
	// Main Method Start
	public static void main(String[] args)
	{
		int i=1; // 초기식
		do{
			System.out.println("3*"+i+"="+(i*3));
			i++; // 증감식
		}while(i<10); // 조건식
		// do While 문의 경우 무조건 1회이상 실행하게 되며, 초기화를 진행하는 반복에서 주로 사용되는 반복문이다.
	}
	// Main Method End
}
