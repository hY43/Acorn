// StatementEx12 Class Start.
public class StatementEx12 {
	// Main Method Start.
	public static void main(String[] args) {
		for (int j = 1; j <= 3; j++) {
			for (int i = 1; i <= 4; i++) {
				System.out.println("i: " + i + " j: "+ j);
				// j의 한자리 숫자당 i를 1~4까지 출력.
			}
		}
	}
	// Main Method End.
}
