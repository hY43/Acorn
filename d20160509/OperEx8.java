package d20160509;
// 8 순위: 대입 연산자.
// OperEx8 Class Start
public class OperEx8 {
	// Main Method Start
	public static void main(String[] args)
	{
		int a = 10;
		a++; // 이 Line 종료 후, a변수의 값을 1 증가 시킨다.
		System.out.println("a: " + a); // a 변수의 값을 출력한다.
		a+=2; // a = a+2
		System.out.println("a: " + a);
		a-=2; // a = a-2
		System.out.println("a: " + a);
	}
	// Main Method End
}
// OperEx8 Class End