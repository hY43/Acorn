package d20160504;

// 2 순위: 산술 연산자
public class OperEx2 {
	public static void main(String[] args) {
		int a = 20;
		int b = 10;

		System.out.println(a + b); // int Type의 변수 a의 값과 b의 합
		System.out.println(a - b); // int Type의 변수 a의 값과 b의 차
		System.out.println(a * b); // int Type의 변수 a의 값과 b의 곱
		System.out.println(a / b); // int Type의 변수 a의 값과 b의 나누기
		System.out.println(a % b); // int Type의 변수 a의 값과 b의 나눈 몫
	}
}
